package uz.ilmnajot.school.enums;

public enum QuestionType {

    MULTIPLE_CHOICE,   // Questions with multiple options, only one of which is correct
    TRUE_FALSE,        // Questions that can be answered with True or False
    SHORT_ANSWER,      // Questions that require a short text-based answer
    ESSAY,             // Questions that require a longer, more detailed answer
    FILL_IN_THE_BLANK, // Questions where the user fills in missing words or phrases
    MATCHING           // Questions where items need to be matched correctly
}
